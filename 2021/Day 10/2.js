function getMiddleScore(data) {
	const pairs = {
		'(': ')',
		'[': ']',
		'{': '}',
		'<': '>'
	}
	const scores = {
		'(': 1,
		'[': 2,
		'{': 3,
		'<': 4,
	}
	const incompleteChunks = data.map(chunks => {
		const openChunks = [];

		for(const chunk of chunks) {
			switch(chunk) {
				case '(':
				case '[':
				case '{':
				case '<':
					openChunks.push(chunk);
					break;
				default:
					if(chunk !== pairs[openChunks.pop()]) {
						return null;
					}
			}
		}

		return openChunks.reverse().reduce((acc, cur) => {
			return (acc * 5) + scores[cur];
		}, 0);
	})
	.filter(scores => scores)
	.sort((a, b) => a - b);

	return incompleteChunks[Math.floor(incompleteChunks.length / 2)];
}


